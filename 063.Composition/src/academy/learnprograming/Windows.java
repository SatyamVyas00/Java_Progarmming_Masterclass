package academy.learnprograming;

public class Windows {
    private int no_of_windows;
    private int no_of_curtains;

    public Windows(int no_of_windows, int no_of_curtains) {
        this.no_of_windows = no_of_windows;
        this.no_of_curtains = no_of_curtains;
    }

    public int getNo_of_windows() {
        return no_of_windows;
    }

    public int getNo_of_curtains() {
        return no_of_curtains;
    }
}
